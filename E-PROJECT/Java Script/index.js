// ADD TO CART
let cartItems = {};
let totalPrice = 0;

function addToCart(productId, price) {
  if (cartItems[productId]) {
    cartItems[productId].quantity++;
    cartItems[productId].totalPrice += price;
  } else {
    cartItems[productId] = {
      quantity: 1,
      totalPrice: price
    };
  }
  
  updateCart();
}

function updateCart() {
    totalPrice= 0;
  let cartItemsHtml = '';
  for (let productId in cartItems) {
    cartItemsHtml += `<p>${productId} x ${cartItems[productId].quantity} = $${cartItems[productId].totalPrice.toFixed(2)}</p>`;
    totalPrice += cartItems[productId].totalPrice;
  }
  cartItemsHtml += `<p>Total: $${totalPrice.toFixed(2)}</p>`;
  document.getElementById("cart-items").innerHTML = cartItemsHtml;
}


